    public HistorialP() {
        initComponents();
        setLocationRelativeTo(null);
        this.setTitle("");
        setResizable(false);
        this.Tabla1.setModel(modelo);
        
        String[] columnas = {
    "IdHistorial", "IdCita", "Fecha", "Hora", "Motivo", 
    "Operacion", "EstadoAnterior", "EstadoNuevo", "FechaOperacion"
};

String[] registros = new String[150];
DefaultTableModel modelo = new DefaultTableModel(null, columnas);

try {
    conn = Main.Enlace(conn);
    s = conn.createStatement();

    // Pedir DNI del paciente
    String dniPaciente = JOptionPane.showInputDialog("Ingrese su DNI para ver su historial m√©dico");

    // Consulta SQL actualizada: une HISTORIAL + CITAMEDICA + PACIENTE
    String sql = "SELECT H.IdHistorial, H.IdCita, C.Fecha, C.Hora, C.Motivo, " +
                 "H.Operacion, H.EstadoAnterior, H.EstadoNuevo, H.FechaOperacion " +
                 "FROM HISTORIAL H " +
                 "JOIN CITAMEDICA C ON H.IdCita = C.IdCita " +
                 "JOIN PACIENTE P ON C.IdPaciente = P.IdPaciente " +
                 "WHERE P.NumeroDNI = '" + dniPaciente + "' " +
                 "ORDER BY H.FechaOperacion DESC";

    rs = s.executeQuery(sql);

    boolean encontrado = false;

    while (rs.next()) {
        registros[0] = rs.getString("IdHistorial");
        registros[1] = rs.getString("IdCita");
        registros[2] = rs.getString("Fecha");
        registros[3] = rs.getString("Hora");
        registros[4] = rs.getString("Motivo");
        registros[5] = rs.getString("Operacion");
        registros[6] = rs.getString("EstadoAnterior");
        registros[7] = rs.getString("EstadoNuevo");
        registros[8] = rs.getString("FechaOperacion");

        modelo.addRow(registros);
        encontrado = true;
    }

    Tabla1.setModel(modelo);

    if (!encontrado) {
        JOptionPane.showMessageDialog(null, "No tienes historial registrado. Verifica tu DNI.");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e + " ERROR AL CONSULTAR HISTORIAL");
}

            
        //}
    }

